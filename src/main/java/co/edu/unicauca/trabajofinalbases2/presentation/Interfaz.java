    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        
        int numRegistro = Integer.parseInt(txtGenerar.getText());

        if (numRegistro < 1) {
            JOptionPane.showMessageDialog(jTabbedPane3, "No se puede generar 0 Registros,",
                    "Error de Registro", JOptionPane.ERROR_MESSAGE);
           
        }else{
            this.arc.eliminarArchivo(ruta);
            crearArchivo(numRegistro);
        }
    }//GEN-LAST:event_btnGenerarActionPerformed

    private void validaIngresoNumeros(java.awt.event.KeyEvent evt) {
        char c = evt.getKeyChar();
        if (c < '0' || c > '9') {
            evt.consume();
        }
    }

    private boolean validaIdUnico(int id) {
        boolean validarId = true;
        actualizarLista();
        for (String dato : ListDatos) {
            String[] dat = dato.split(" ");
            if (id == Integer.parseInt(dat[0])) {
                validarId = false;
                break;
            }
        }
        return validarId;
    }

    private void actualizarLista() {
        ListDatos = arc.leer(ruta);
    }

    private void cargarArbol() {
        actualizarLista();
        String[] dat = null;
        for (String list : ListDatos) {
            dat = list.split(" ");
            arbol.insertar(Integer.parseInt(dat[0]));
        }
    }

