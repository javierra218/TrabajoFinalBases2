    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        int codigo = Integer.parseInt(txtIngresoCodigo.getText());
        int valor = Integer.parseInt(txtIngresoValor.getText());
        boolean validar = true;

        if (validaIdUnico(codigo)) {
            if (codigo < 1 || codigo > 1000000) {
                JOptionPane.showMessageDialog(jTabbedPane3, "El codigo debe ser un número entero entre 1 y 1000000,",
                        "Error de ingreso", JOptionPane.ERROR_MESSAGE);
                validar = false;
            }
            if (valor < 1 || valor > 1000000) {
                JOptionPane.showMessageDialog(jTabbedPane3, "El valor debe ser un número entero entre 1 y 1000000,",
                        "Error de ingreso", JOptionPane.ERROR_MESSAGE);
                validar = false;
            }

            if (validar) {
                this.arc.escribir(ruta, codigo + " " + valor);
                JOptionPane.showMessageDialog(rootPane, "Se registraron valores correctamente");
                txtIngresoCodigo.setText("");
                txtIngresoValor.setText("");
                cargarArbol();
            }
        } else {
            JOptionPane.showMessageDialog(jTabbedPane3, "El codigo ya existe,",
                    "codigo duplicado", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_btnAgregarActionPerformed

