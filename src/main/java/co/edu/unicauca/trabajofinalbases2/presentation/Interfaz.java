    private void crearArchivo(int n) {

        if (n > 0) {
            int[] claves = null;
            int[] valores = null;

            valores = ThreadLocalRandom.current().ints(1, 1000000).limit(n).toArray();

            claves = (ThreadLocalRandom.current().ints(1, 1000000).distinct().limit(n).toArray());
            for (int i = 0; i < claves.length; i++) {
                this.arc.escribir(ruta, claves[i] + " " + valores[i]);
            }
            JOptionPane.showMessageDialog(null, "Carga exitosa: ");
            
        }

        cargarArbol();
    }
    
    
