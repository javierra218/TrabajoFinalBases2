    private void btnBuscarNoIndexadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarNoIndexadaActionPerformed
        int idBuscar = Integer.parseInt(txtBuscarCodigo.getText());
        long tiempoInicio, tiempoFinal, tiempoTotal;

        tiempoInicio = System.currentTimeMillis();

        String linea = arc.leerLineaPorDato(ruta, idBuscar);

        tiempoFinal = System.currentTimeMillis();
        tiempoTotal = tiempoFinal - tiempoInicio;

        if (linea.equalsIgnoreCase("El dato no se encuentra en el archivo")) {
            JOptionPane.showMessageDialog(jTabbedPane3, "EL dato " + idBuscar + " no se encuentra\n"
                    + " TIEMPO DE SALIDA DE LA BUSQUEDA NO INDEXADA ES: " + tiempoTotal + " milisegundos", "No Found", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(rootPane, linea
                    + " TIEMPO DE SALIDA DE LA BUSQUEDA NO INDEXADA ES: " + tiempoTotal + " milisegundos");
        }
    }//GEN-LAST:event_btnBuscarNoIndexadaActionPerformed

    private void btnBuscarIndexadaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarIndexadaActionPerformed
        int codigoBuscar = Integer.parseInt(txtBuscarCodigo.getText());
        long tiempoInicio, tiempoFinal, tiempoTotal;

        tiempoInicio = System.currentTimeMillis();

        int posicion = arbol.estaBMas(codigoBuscar);

        tiempoFinal = System.currentTimeMillis();
        tiempoTotal = tiempoFinal - tiempoInicio;
        if (posicion >= 0) {
            JOptionPane.showMessageDialog(rootPane, "El dato " + codigoBuscar + " esta en la posicion " + (posicion + 1) + " del arbol\n"
                    + " TIEMPO DE SALIDA DE LA BUSQUEDA INDEXADA ES: " + tiempoTotal + " milisegundos");
        } else {
            JOptionPane.showMessageDialog(jTabbedPane3, "EL dato " + codigoBuscar + " no se encuentra\n"
                    + " TIEMPO DE SALIDA DE LA BUSQUEDA INDEXADA ES: " + tiempoTotal + " milisegundos", "No Found", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarIndexadaActionPerformed

